#include <iostream>

using namespace std;

int main(){
	float num1, num2, divisao;
	
	
	
	
	cout << "Digite o primeiro numero: ";
	cin >> num1;
	
	cout << "Digite o segundo numero: ";
	cin >> num2;
	
	divisao = num1 / num2;
	
	cout << "Soma: " << num1 + num2 << "\n";
	cout << "Subtracao: " << num1 - num2 << "\n";
	cout << "Multiplicao: " << num1 * num2 << "\n";
	cout << "Divisao: " << divisao << "\n";
	 
	
	
	return 0;
}
